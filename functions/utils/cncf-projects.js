// すべてのプロジェクトを結合（名前のみのリスト）
export const CNCF_PROJECTS = [
  ...GRADUATED_PROJECTS.map(p => p.name),
  ...INCUBATING_PROJECTS.map(p => p.name),
  ...SANDBOX_PROJECTS.map(p => p.name)
];

// プロジェクト詳細情報を取得する関数
export function getProjectDetails(name) {
  const allProjects = [...GRADUATED_PROJECTS, ...INCUBATING_PROJECTS, ...SANDBOX_PROJECTS];
  return allProjects.find(p => p.name === name);
}

/**
 * ランダムにCNCFプロジェクトを選択
 */
export function getRandomCNCFProject() {
  const project = CNCF_PROJECTS[Math.floor(Math.random() * CNCF_PROJECTS.length)];
  const details = getProjectDetails(project);
  return details ? `${details.name} - ${details.description}` : project;
}

